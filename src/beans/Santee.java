package beans;

public class Santee {
	private int medecin_generaliste;
	private int dentiste;
	private double score;
	public double getScore() {
		return score;
	}
	public void setScore(double score) {
		this.score = score;
	}
	public int getMedecin_generaliste() {
		return medecin_generaliste;
	}
	public void setMedecin_generaliste(int medecin_generaliste) {
		this.medecin_generaliste = medecin_generaliste;
	}
	public int getDentiste() {
		return dentiste;
	}
	public void setDentiste(int dentiste) {
		this.dentiste = dentiste;
	}
}
